#include "cub3d.h"

void	print_list(t_lmap *lmap)
{
	int	i;

	i = 0;
	while (lmap)
	{
		ft_printf("%s\n", lmap->cont);
		lmap = lmap->next;
		i++;
	}
	ft_printf("\n\n");
}
